class SwitchCase 
{
	public static void main(String[] args) 
	{
		char Alpha = 'a';
		switch (Alpha){
			case 'a':
			{
				System.out.println("it is vowel");
				break;
			}
			case 'e':
			{
				System.out.println("it is vowel");
				break;
			}
			case 'i':
			{
				System.out.println("it is vowel");
			}
			case 'o':
			{
				System.out.println("It is vowel");
				break;
			}
			case 'u':
			{
				System.out.println("It is vowel");
				break;
			}
			default:
			{
				System.out.println("It is not vowel");
			}
			
		}
	}
}
