class Star_Pyramid
{
	public static void main(String[] args) 
	{
		int n=11;
		for (int a=1; a<=n;a++ )
		{												   
			for (int b=1;b<=a ;b++ )        			   
			{         									   
                System.out.print("* ");
													           
			}
			System.out.println();
		}
	}
}

